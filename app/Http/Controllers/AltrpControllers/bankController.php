    /**
     * bankController constructor.
     */
    public function __construct()
    {
        $this->modelClass = bank::class;
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(bankStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $bank = new bank;

        $regChat = ChatService::registerInChat($bank->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($bank->getTable(), $data);

        $bank->fill($data);

        $res = $bank->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$bank], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $bank = bank::find($id);

        if(! $bank) {
            return response()->json(trans("responses.not_found.bank"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'bank')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $bank = $bank->load($relations);
        }

        $bank = $this->getRemoteData($model, $bank, true);

        return response()->json($bank, 200, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(bankUpdateRequest $request, $id)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $bank = bank::find($id);

        if(! $bank) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.bank")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $data = $this->hashPasswordAttributeIfExists($bank->getTable(), $data);

        $result = $bank->update($data);

        if($result){
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $bank = bank::find($id);

        if(! $bank) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.bank")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $bank->delete();

        if($result) {
            return response()->json(['success'=>true, 'message' => trans("responses.delete.bank")], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("deleteerror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update column of resource from storage.
     *
     * @param ApiRequest $request
     * @param $id
     * @param $column
     * @return \Illuminate\Http\JsonResponse
     */
    public function updateColumn(ApiRequest $request, $id, $column)
    {
        $bank = bank::find($id);

        if(! $bank) {
            return response()->json(['success'=>false, 'message' =>"bank not found"], 404, [], JSON_UNESCAPED_UNICODE);
        }

        if (! isset($bank->$column)) {
            return response()->json(['success'=>false, 'message'=>'Column not exists'], 400, [], JSON_UNESCAPED_UNICODE);
        }

        $bank->$column = $request->column_value;

        if ($bank->save()) {
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json('Failed to update', 400, [], JSON_UNESCAPED_UNICODE);
    }

    public function getIndexedColumnsValueWithCount($column)
    {
        $banks = bank::selectRaw("$column as value, COUNT($column) as count")
                            ->havingRaw("COUNT($column)")
                            ->groupBy("$column")
                            ->get();
        return response()->json($banks, 200, [], JSON_UNESCAPED_UNICODE);
    }

