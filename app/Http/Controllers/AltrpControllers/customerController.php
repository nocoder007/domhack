    /**
     * customerController constructor.
     */
    public function __construct()
    {
        $this->modelClass = customer::class;
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(customerStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $customer = new customer;

        $regChat = ChatService::registerInChat($customer->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($customer->getTable(), $data);

        $customer->fill($data);

        $res = $customer->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$customer], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $customer = customer::find($id);

        if(! $customer) {
            return response()->json(trans("responses.not_found.customer"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'customer')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $customer = $customer->load($relations);
        }

        $customer = $this->getRemoteData($model, $customer, true);

        return response()->json($customer, 200, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(customerUpdateRequest $request, $id)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $customer = customer::find($id);

        if(! $customer) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.customer")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $data = $this->hashPasswordAttributeIfExists($customer->getTable(), $data);

        $result = $customer->update($data);

        if($result){
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $customer = customer::find($id);

        if(! $customer) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.customer")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $customer->delete();

        if($result) {
            return response()->json(['success'=>true, 'message' => trans("responses.delete.customer")], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("deleteerror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update column of resource from storage.
     *
     * @param ApiRequest $request
     * @param $id
     * @param $column
     * @return \Illuminate\Http\JsonResponse
     */
    public function updateColumn(ApiRequest $request, $id, $column)
    {
        $customer = customer::find($id);

        if(! $customer) {
            return response()->json(['success'=>false, 'message' =>"customer not found"], 404, [], JSON_UNESCAPED_UNICODE);
        }

        if (! isset($customer->$column)) {
            return response()->json(['success'=>false, 'message'=>'Column not exists'], 400, [], JSON_UNESCAPED_UNICODE);
        }

        $customer->$column = $request->column_value;

        if ($customer->save()) {
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json('Failed to update', 400, [], JSON_UNESCAPED_UNICODE);
    }

    public function getIndexedColumnsValueWithCount($column)
    {
        $customers = customer::selectRaw("$column as value, COUNT($column) as count")
                            ->havingRaw("COUNT($column)")
                            ->groupBy("$column")
                            ->get();
        return response()->json($customers, 200, [], JSON_UNESCAPED_UNICODE);
    }

